package Project_package;

public class Usertest {
	int ID;
	String nazwisko;
	
	public Usertest(int ID, String name){
		this.ID = ID;
		this.nazwisko = name;
	}
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getNazwisko() {
		return nazwisko;
	}
	public void setNazwisko(String nazwisko) {
		this.nazwisko = nazwisko;
	}
	
	

}
